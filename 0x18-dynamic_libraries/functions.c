#include <stdio.h>

/**
 * add - function to add 2 numbers
 * @x: 1st number
 * @y: 2nd number
 * Return: sum of x and y
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - function to subtract 2 numbers
 * @x: 1st number
 * @y: 2nd number
 * Return: subtraction of x and y
 */
int sub(int x, int y)
{
	return (x - y);
}

/**
 * mul - function to multiply 2 numbers
 * @x: 1st number
 * @y: 2nd number
 * Return: multiplicaation of x and y
 */
int mul(int x, int y)
{
        return (x * y);
}

/**
 * div - function to divide 2 numbers
 * @x: 1st number
 * @y: 2nd number
 * Return: division of x and y
 */
int div(int x, int y)
{
        return (x / y);
}

/**
 * mod - function to return modulus of 2 numbers
 * @x: 1st number
 * @y: 2nd number
 * Return: modulus of x and y
 */
int mod(int x, int y)
{
        return (x % y);
}
